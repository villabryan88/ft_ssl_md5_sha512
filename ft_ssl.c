#include <ft_ssl.h>

int		main(int ac, char **av)
{
	unsigned char *digest;
	int		i = 0;

	if (ac == 2)
	{
		digest = md5(av[1], NULL);
	}
	while (i < 16)	{
		ft_printf("%.2hhx", *(digest + i));
		i++;
	}

	return (0);

}
